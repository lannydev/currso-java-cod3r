package Fundamentos;

public class OperadoresAritmeticos {
    public static void main(String[] args) {

        double x = 2.5;
        double y = 4.3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        int a = 8;
        int b = 6;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * (double) b);
        System.out.println(a / (double)b);
    }
}
